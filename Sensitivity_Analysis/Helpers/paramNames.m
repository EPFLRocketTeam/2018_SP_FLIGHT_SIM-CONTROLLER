function [rocketparamIDs, envparamIDs] = paramNames(SimObj)
%PARAMNAMES gives the names of the parameters that can be modified in the given simulator.
%   INPUT
%       SimObj          Simulator object
%   OUTPUTS
%       rocketparamIDs  Names of the rocket parameters
%       envparamIDs     Names of the environments parameters

if class(SimObj) == "multilayerwindSimulator3D" ... 
   || class(SimObj) == "multilayerwindSimulator3D_ode45" ... 
   || class(SimObj) == "multilayerwindSimulator3D_ode23" ...
   || class(SimObj) == "multilayerwindSimulator3D_ode113" ...
   || class(SimObj) == "multilayerwindSimulator3D_ode15s" ...
   || class(SimObj) == "multilayerwindSimulator3D_ode23s" ...
